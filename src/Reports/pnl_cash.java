        int col = jTable1.columnAtPoint(evt.getPoint());

        String valueInCell = (String)jTable1.getValueAt(row, col);

        String id = dataCash.dataTableShow.getvalue(row, dataCash.ID);
        String data = dataCash.dataTableShow.getvalue(row, dataCash.DATE);
